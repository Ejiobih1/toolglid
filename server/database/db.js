const { Pool } = require('pg');
require('dotenv').config();

// PostgreSQL connection pool
// Supports both connection URL (for Supabase, Neon, Railway) and individual parameters
const pool = new Pool(
  process.env.DATABASE_URL
    ? {
        connectionString: process.env.DATABASE_URL,
        ssl: {
          rejectUnauthorized: false // Required for cloud databases
        },
        max: 20,
        idleTimeoutMillis: 30000,
        connectionTimeoutMillis: 2000,
      }
    : {
        host: process.env.DB_HOST || 'localhost',
        port: process.env.DB_PORT || 5432,
        database: process.env.DB_NAME || 'pdf_tools_db',
        user: process.env.DB_USER || 'postgres',
        password: process.env.DB_PASSWORD || 'postgres',
        max: 20,
        idleTimeoutMillis: 30000,
        connectionTimeoutMillis: 2000,
      }
);

// Test database connection
pool.on('connect', () => {
  console.log('‚úÖ Connected to PostgreSQL database');
});

pool.on('error', (err) => {
  console.error('‚ùå Unexpected database error:', err);
  process.exit(-1);
});

// Helper function to execute queries
const query = async (text, params) => {
  const start = Date.now();
  try {
    const res = await pool.query(text, params);
    const duration = Date.now() - start;
    console.log('üìä Executed query', { text, duration, rows: res.rowCount });
    return res;
  } catch (error) {
    console.error('‚ùå Query error:', error);
    throw error;
  }
};

// Get a client from the pool (for transactions)
const getClient = () => {
  return pool.connect();
};

// Test connection function
const testConnection = async () => {
  try {
    const result = await query('SELECT NOW()');
    console.log('‚úÖ Database connection test successful:', result.rows[0]);
    return true;
  } catch (error) {
    console.error('‚ùå Database connection test failed:', error.message);
    return false;
  }
};

module.exports = {
  query,
  getClient,
  testConnection,
  pool
};
